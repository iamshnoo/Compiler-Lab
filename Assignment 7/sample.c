int main(){
    int a,b,s,d;
    
    // Assignment
    a=(b*3+(4*a+-s))/d;
    
    // While
    while(3+a*b){
        b=b-1;
        a=a-1;
    }

    // If
    if(s=a*b){
        a=a-1;
        b=b-a/2;
    }

    // If else
    if(a/2-s){
        d=d*2;
        a=b+2;
    } else{
        a=a+3;
        s=s-1;
    }

    return 0;
}